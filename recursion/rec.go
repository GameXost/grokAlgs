package recursion

func Sum(nums []int) int {
	if len(nums) == 0 {
		return 0
	}
	if len(nums) == 1 {
		return nums[0]
	}
	return nums[0] + Sum(nums[1:])
}

func Greatest(nums []int) int {
	if len(nums) == 0 {
		return 0
	}
	if len(nums) == 1 {
		return nums[0]
	}
	return max(nums[0], Greatest(nums[1:]))
}
